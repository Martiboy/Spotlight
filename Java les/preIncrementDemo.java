

// het ++ teken is een preIncrement oftewel......
import static java.lang.System.out;
class preIncrementDemo {
	public static void main(String args[]){
		int konijnen = 27; // je heb hier 27 konijnen

		++konijnen; // hiermee tel je er 1 bij op
		out.println(konijnen); //28 konijnen
		out.println(++konijnen); // nu maak je er 29 van
		out.println(konijnen);






	}

}
