
import static java.lang.System.out;

class UseAssignmentOperators {

	public static void main(String args[]){
		double konijnen = 27;
		double	numberExtra = 53; //extra nummer voor de lol

		konijnen += 1; //27+1
		out.println(konijnen);

		konijnen *= 50; //28*50
		out.println(konijnen);

		konijnen /= numberExtra; //1400/53
		out.println(konijnen);

		konijnen = 100;
		out.println(konijnen); //100





	}
}
