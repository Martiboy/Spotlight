import static java.lang.System.out;
class postIncrementDemo {
	public static void main(String args[]){

	int konijnen =27; //we nemen 27 konijnen

		konijnen++; //en we maken er 28 van
		out.println(konijnen);
		out.println(konijnen++); //hij geeft nu 28 konijnen weer en telt er daarna 1 bij op
		out.println(konijnen); //nu zijn het er 29
	}
}
